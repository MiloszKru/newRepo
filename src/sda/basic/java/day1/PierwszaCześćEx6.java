package sda.basic.java.day1;

public class PierwszaCześćEx6 {

    public static void main(String[] args) {

        int multiplier = 5;
        int multiplierMin = 1;
        int multiplierMax = 4;


        for (int i = multiplierMin; i <= multiplierMax; i++) {
            System.out.println(multiplier + " x " + i + " = " + multiplier * i);
        }
    }
}
